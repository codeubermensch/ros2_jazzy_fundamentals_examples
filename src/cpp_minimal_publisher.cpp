#include "rclcpp/rclcpp.hpp" //ROS 2 client library
#include "std_msgs/msg/string.hpp" //standard message type for strings
using namespace std::chrono_literals; // handles time duration

class MinimalCppPublisher : public rclcpp::Node
{
public:
  MinimalCppPublisher() : Node("minimal_cpp_publisher"), count_(0)
  {
    publisher_ = create_publisher<std_msgs::msg::String>("/cpp_example_topic",10);

    timer_ = create_wall_timer(1s,
    std::bind(&MinimalCppPublisher::timerCallback, this));

    RCLCPP_INFO(get_logger(), "Publishing at 1hz");

  }

  void timerCallback(){
    auto message = std_msgs::msg::String();
    message.data = "Namshkar Doston! " + std::to_string(count_++);

    publisher_-> publish(message);
  }

private:
  size_t count_;
  rclcpp::Publisher<std_msgs::msg::String>::SharedPtr
      publisher_;//the publisher object
  rclcpp::TimerBase::SharedPtr timer_; //timer
};

int main(int argc, char *argv[]){

    rclcpp::init(argc, argv);
    auto minimal_cpp_publisher = std::make_shared<MinimalCppPublisher>();
    rclcpp::spin(minimal_cpp_publisher);

    rclcpp::shutdown();

    return 0;
}
